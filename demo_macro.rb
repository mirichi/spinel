# Dummy definitions of Macronel and MacronelTable for Spinel type inference
module Macronel
  def self.node_content(nid); ""; end
  def self.node_type(nid); ""; end
  def self.node_name(nid); ""; end
  def self.node_body(nid); 0; end
  def self.node_arguments(nid); 0; end
  def self.node_block(nid); 0; end
  def self.get_stmts(nid); []; end
  def self.get_args(nid); []; end
  def self.to_ruby(nid); ""; end
  def self.embed_string(path); ""; end
  def self.brainfuck(bf); ""; end
  def self.lisp(code); ""; end
  def self.register_helper(name); end
  class << self
    attr_accessor :ast_table
  end
end

class MacronelTable
  def nd_stmts; [""]; end
end

class LispNode
  def node_type
    @node_type.to_s
  end
  def node_type=(val)
    @node_type = val.to_s
  end

  def children
    res = @children
    res
  end
  def children=(val)
    @children = val
  end

  def sym_val
    @sym_val.to_s
  end
  def sym_val=(val)
    @sym_val = val.to_s
  end

  def int_val
    @int_val.to_i
  end
  def int_val=(val)
    @int_val = val.to_i
  end

  def initialize
    @node_type = ""
    @children = [self]
    @children.clear
    @sym_val = ""
    @int_val = 0
  end
end

module MacronelMacros
  register_helper :LispNode
  register_macro :embed_string
  register_macro :config
  register_macro :html_template
  register_macro :brainfuck
  register_macro :lisp

  def self.embed_string(file_path_nid)
    path = Macronel.node_content(file_path_nid).to_s
    content = File.read(path).to_s
    content.inspect.to_s
  end

  def self.config(key_nid)
    key = Macronel.node_content(key_nid).to_s
    content = File.read("config.json").to_s
    target = "\"" + key + "\""
    idx = content.index(target)
    return '""' if idx.nil? || idx < 0
    
    sub = ""
    start = idx + target.length
    i = start
    while i < content.length
      sub += content[i].to_s
      i = i + 1
    end
    
    val_start = sub.index("\"")
    return '""' if val_start.nil? || val_start < 0
    
    val_sub = ""
    start2 = val_start + 1
    i = start2
    while i < sub.length
      val_sub += sub[i].to_s
      i = i + 1
    end
    
    val_end = val_sub.index("\"")
    return '""' if val_end.nil? || val_end < 0
    
    val = ""
    i = 0
    while i < val_end
      val += val_sub[i].to_s
      i = i + 1
    end
    res_val = ""
    res_val = res_val + "\"" + val.to_s + "\""
    res_val.to_s
  end

  def self.html_template(block_nid)
    body_id = Macronel.node_body(block_nid).to_i
    compile_html_node(body_id).to_s
  end

  def self.compile_html_node(nid)
    nid = nid.to_i
    return '""' if nid < 0
    type = Macronel.node_type(nid).to_s
    if type == "StatementsNode"
      stmts = Macronel.get_stmts(nid)
      if stmts.empty?
        return '""'
      else
        res = ""
        i = 0
        while i < stmts.length
          res += compile_html_node(stmts[i].to_i).to_s
          res += " + " if i < stmts.length - 1
          i = i + 1
        end
        return res.to_s
      end
    elsif type == "CallNode"
      name = Macronel.node_name(nid).to_s
      args_id = Macronel.node_arguments(nid).to_i
      args = args_id != -1 ? Macronel.get_args(args_id) : []
      block_id = Macronel.node_block(nid).to_i
      
      inner = '""'
      if block_id != -1
        inner = compile_html_node(Macronel.node_body(block_id).to_i).to_s
      elsif !args.empty?
        inner = Macronel.to_ruby(args[0].to_i).to_s
      end
      
      res = ""
      res = res + '"<' + name + '>" + ' + inner + ' + "</' + name + '>"'
      return res.to_s
    else
      return Macronel.to_ruby(nid).to_s
    end
  end

  def self.brainfuck(bf_nid)
    bf = Macronel.node_content(bf_nid).to_s
    compile_bf_to_ruby(bf).to_s
  end

  def self.compile_bf_to_ruby(bf)
    bf = bf.to_s
    bf_chars = [""]
    bf_chars.clear
    i = 0
    while i < bf.length
      c = bf[i].to_s
      bf_chars << c if c == ">" || c == "<" || c == "+" || c == "-" || c == "." || c == "," || c == "[" || c == "]"
      i += 1
    end
    
    code = "ptr = 0\nmem = []\n"
    code = code + "i = 0\nwhile i < 30000\n  mem.push(0)\n  i = i + 1\nend\n"
    
    indent = ""
    i = 0
    n = bf_chars.length
    while i < n
      c = bf_chars[i].to_s
      if c == "+"
        count = 0
        while i < n && bf_chars[i].to_s == "+"
          count += 1
          i += 1
        end
        code = code + indent + "mem[ptr] = mem[ptr] + " + count.to_s + "\n"
        next
      elsif c == "-"
        count = 0
        while i < n && bf_chars[i].to_s == "-"
          count += 1
          i += 1
        end
        code = code + indent + "mem[ptr] = mem[ptr] - " + count.to_s + "\n"
        next
      elsif c == ">"
        count = 0
        while i < n && bf_chars[i].to_s == ">"
          count += 1
          i += 1
        end
        code = code + indent + "ptr = ptr + " + count.to_s + "\n"
        next
      elsif c == "<"
        count = 0
        while i < n && bf_chars[i].to_s == "<"
          count += 1
          i += 1
        end
        code = code + indent + "ptr = ptr - " + count.to_s + "\n"
        next
      elsif c == "."
        code = code + indent + "print mem[ptr].chr\n"
      elsif c == ","
        # no-op
      elsif c == "["
        code = code + indent + "while mem[ptr] != 0\n"
        indent = indent + "  "
      elsif c == "]"
        indent = indent[0...-2] || ""
        code = code + indent + "end\n"
      end
      i += 1
    end
    code
  end

  def self.lisp(lisp_code_nid)
    code = Macronel.node_content(lisp_code_nid).to_s
    compile_lisp_string(code).to_s
  end

  def self.tokenize_lisp(code)
    code = code.to_s
    tokens = [""]
    tokens.clear
    curr = ""
    i = 0
    while i < code.length
      c = code[i].to_s
      if c == "(" || c == ")"
        if curr != ""
          tokens.push(curr.to_s)
          curr = ""
        end
        tokens.push(c.to_s)
      elsif c == " " || c == "\n" || c == "\r" || c == "\t"
        if curr != ""
          tokens.push(curr.to_s)
          curr = ""
        end
      else
        curr += c.to_s
      end
      i = i + 1
    end
    if curr != ""
      tokens.push(curr.to_s)
    end
    tokens
  end

  def self.parse_lisp_tokens(tokens, idx_box)
    idx = idx_box[0].to_i
    return nil if idx >= tokens.length
    
    token = tokens[idx].to_s
    idx_box[0] = idx + 1
    
    node = LispNode.new
    if token == "("
      node.node_type = "list"
      while idx_box[0].to_i < tokens.length && tokens[idx_box[0].to_i].to_s != ")"
        child = parse_lisp_tokens(tokens, idx_box)
        node.children.push(child) if child != nil
      end
      idx_box[0] = idx_box[0].to_i + 1 # discard ")"
      node
    elsif token == ")"
      nil
    else
      is_num = true
      start_idx = 0
      if token[0].to_s == "-" && token.length > 1
        start_idx = 1
      end
      if token.length == 0
        is_num = false
      end
      j = start_idx
      while j < token.length
        c = token[j].to_s
        if c != "0" && c != "1" && c != "2" && c != "3" && c != "4" && c != "5" && c != "6" && c != "7" && c != "8" && c != "9"
          is_num = false
        end
        j = j + 1
      end
      
      if is_num
        node.node_type = "integer"
        node.int_val = token.to_i
      else
        node.node_type = "symbol"
        node.sym_val = token.to_s
      end
      node
    end
  end

  def self.compile_lisp_string(code)
    tokens = tokenize_lisp(code)
    exprs = [LispNode.new]
    exprs.clear
    idx_box = [0]
    while idx_box[0].to_i < tokens.length
      expr = parse_lisp_tokens(tokens, idx_box)
      exprs.push(expr) if expr != nil
    end
    
    res = ""
    i = 0
    while i < exprs.length
      res += compile_lisp_expr(exprs[i]).to_s
      res += "\n" if i < exprs.length - 1
      i = i + 1
    end
    res.to_s
  end

  def self.compile_lisp_expr(node)
    return "" if node == nil
    
    if node.node_type == "list"
      children = node.children
      return "" if children.empty?
      
      op_node = children[0]
      return "" if op_node == nil
      op = op_node.sym_val.to_s
      
      if op == "defun"
        name_node = children[1]
        return "" if name_node == nil
        name = name_node.sym_val.to_s
        
        args_node = children[2]
        args_str = ""
        if args_node != nil && args_node.node_type == "list"
          i = 0
          while i < args_node.children.length
            arg = args_node.children[i]
            if arg != nil
              args_str += arg.sym_val.to_s
              args_str += ", " if i < args_node.children.length - 1
            end
            i = i + 1
          end
        end
        
        body_node = children[3]
        body = compile_lisp_expr(body_node).to_s
        
        res = ""
        res = res + "def " + name + "(" + args_str + "); " + body + "; end"
        return res.to_s
        
      elsif op == "if"
        cond = compile_lisp_expr(children[1]).to_s
        then_b = compile_lisp_expr(children[2]).to_s
        else_b = compile_lisp_expr(children[3]).to_s
        
        res = ""
        res = res + "((" + cond + ") ? (" + then_b + ") : (" + else_b + "))"
        return res.to_s
        
      elsif op == "+" || op == "-" || op == "*" || op == "/" || op == "<" || op == ">" || op == "<=" || op == ">=" || op == "==" || op == "="
        actual_op = op
        actual_op = "==" if op == "="
        
        res = "("
        i = 1
        while i < children.length
          res += compile_lisp_expr(children[i]).to_s
          res = res + " " + actual_op + " " if i < children.length - 1
          i = i + 1
        end
        res += ")"
        return res.to_s
        
      else
        func = op
        res = ""
        res = res + func + "("
        i = 1
        while i < children.length
          res += compile_lisp_expr(children[i]).to_s
          res += ", " if i < children.length - 1
          i = i + 1
        end
        res += ")"
        return res.to_s
      end
      
    elsif node.node_type == "symbol"
      return node.sym_val.to_s
    elsif node.node_type == "integer"
      return node.int_val.to_s
    else
      return ""
    end
  end
end

